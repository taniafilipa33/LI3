
public class Triplo
{
    private Integer n1;
    private double n2;
    private double n3;
    
    public Triplo(int n1,double n2,double n3) {
        this.n1= n1;
        this.n2=n2;
        this.n3=n3;
    }
    
    public int getN1 () {
        return this.n1;
    }
    
     public double getN2 () {
         return this.n2;
    }
    
     public double getN3 () {
         return this.n3;
    }
}
